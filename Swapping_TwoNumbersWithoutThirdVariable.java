package interviewProgramsFundamentals;

public class Swapping_TwoNumbersWithoutThirdVariable {

	public static void main(String[] args) {
		
		//Swapping without 3rd Variable------->Program1
		
		int a= 20;//10
		int b= 10;//20
		
		a = a-b;//20-10= 10=a
		b = b+a;//10+10= 20=b
		System.out.println("a: "+a);
		System.out.println("b: "+b);
		
		
		
		
		
		
		
		//Swapping without 3rd Variable------->Program2
		int c = 10;
		int d = 20;
		
		d = d-c;//20-10= 10=d
		c = d+c;//10+10= 20=c
		
		System.out.println("c: "+c);
		System.out.println("d: "+d);
		
		
	}
	
}
